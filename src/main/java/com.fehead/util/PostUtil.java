@Component
public class PostUtil {

    private ObjectMapper objectMapper = new ObjectMapper();

    public String sendPost(String url, List<NameValuePair> forParams) throws Exception {
        HttpClient httpClient = null;
        httpClient = new SSLClient();
        HttpPost post = new HttpPost(url);

        HttpEntity reqEntity = new UrlEncodedFormEntity(forParams, "utf-8");

        post.setHeader("Content-Type","application/x-www-form-urlencoded");
        post.setEntity(reqEntity);
        HttpResponse execute = httpClient.execute(post);

        String s = IOUtils.toString(execute.getEntity().getContent());
        System.out.println(s);

        return s;
    }

    public String sendGet(String url) {
        System.out.println("GET: " + url);
        String result = null;
        CloseableHttpClient httpClient = HttpClients.createDefault();
        HttpGet get = new HttpGet(url);
        CloseableHttpResponse response = null;
        try {
            response = httpClient.execute(get);
            if(response != null && response.getStatusLine().getStatusCode() == 200)
            {
                result = IOUtils.toString(response.getEntity().getContent());
                System.out.println(result);
            }
            return result;
        } catch (IOException e) {
            e.printStackTrace();
        }finally {
            try {
                httpClient.close();
                if(response != null)
                {
                    response.close();
                }
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
        return null;
    }
}

class SSLClient extends DefaultHttpClient {

    public SSLClient() throws Exception {
        super();
        //传输协议需要根据自己的判断
        SSLContext ctx = SSLContext.getInstance("TLS");
        X509TrustManager tm = new X509TrustManager() {
            @Override
            public void checkClientTrusted(X509Certificate[] chain,
                                           String authType) throws CertificateException {
            }

            @Override
            public void checkServerTrusted(X509Certificate[] chain,
                                           String authType) throws CertificateException {
            }

            @Override
            public X509Certificate[] getAcceptedIssuers() {
                return null;
            }
        };
        ctx.init(null, new TrustManager[]{tm}, null);
        SSLSocketFactory ssf = new SSLSocketFactory(ctx, SSLSocketFactory.ALLOW_ALL_HOSTNAME_VERIFIER);
        ClientConnectionManager ccm = this.getConnectionManager();
        SchemeRegistry sr = ccm.getSchemeRegistry();
        sr.register(new Scheme("https", 443, ssf));
    }
}
